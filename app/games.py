FAST_WORDS = ["عراق", "فادي", "قمر", "شمس", "مطر", "كتاب", "ذهب"]

active_fast = {}

def make_game(game_name):
    q, answer, wrongs = random.choice(GAMES[game_name])
    choices = [answer] + wrongs
    random.shuffle(choices)

    kb = InlineKeyboardMarkup(row_width=2)
    for c in choices:
        kb.add(
            InlineKeyboardButton(
                c,
                callback_data=f"game:{c}:{answer}"
            )
        )

    return q, kb

def handle_game_answer(bot, call):
    try:
        _, choice, answer = call.data.split(":", 2)

        if choice == answer:
            bot.answer_callback_query(call.id, "✅ صح", show_alert=True)
            bot.edit_message_text(
                f"✅ إجابة صحيحة\nالفائز: {call.from_user.first_name}",
                call.message.chat.id,
                call.message.message_id
            )
        else:
            bot.answer_callback_query(call.id, "❌ خطأ", show_alert=True)

    except:
        bot.answer_callback_query(call.id, "صار خطأ", show_alert=True)

def start_fast(chat_id):
    word = random.choice(FAST_WORDS)
    active_fast[str(chat_id)] = word
    return word

def check_fast(chat_id, text):
    key = str(chat_id)
    if key in active_fast and text == active_fast[key]:
        del active_fast[key]
        return True
    return False
